# Integer
a = 10
print(type(a)) 



# Float
b = 10.5
print(type(b)) 


# Complex
c = 3 + 4j
print(type(c)) 




# String
s = "Hello, World!"
print(type(s)) 


# Tuple
t = (1, 2, 3, 4, 5)
print(type(t)) 



# Set
se = {1, 2, 3, 4, 5}
print(type(se))



# Dictionary
d = {"name": "kumar", "age": 21}
print(type(d)) 


# Boolean
flag = True
print(type(flag))